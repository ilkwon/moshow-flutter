// ----------------------------------------------------------------------------
// 앱 내비게이션
// ----------------------------------------------------------------------------
// 바텀바 5개 탭을 숫자 대신 이름으로 관리.
enum TabType {
  home,
  search,
  upload,
  collect,
  profile,
}

// ----------------------------------------------------------------------------
// API 상태
// ----------------------------------------------------------------------------
// 서버 응답 HTTP 코드를 이름으로 관리
enum ApiStatus {
  success, // 200
  created, // 201
  badRequest, // 400
  unauthorized, // 401
  notFound, // 404
  serverError, // 500
}

// ----------------------------------------------------------------------------
// 사용자
// ----------------------------------------------------------------------------
// 게스트로 시작해서 나중에 소셜 로그인으로 승격
enum UserType {
  guest,
  member,
}

// ----------------------------------------------------------------------------
// 게시물
// ----------------------------------------------------------------------------
// 게시물의 현재 상태
enum PostStatus {
  active, // 정상 노출
  hidden, // 작성자 숨김
  deleted, // 작성자 삭제
  suspended, // 관리자 제재
}

// ----------------------------------------------------------------------------
// 업로드
// ----------------------------------------------------------------------------

// 업로드 진행 상태
enum UploadStatus {
  idle, // 대기 중
  uploading, // 업로드 중
  success, // 완료
  failed, // 실패
}

// ----------------------------------------------------------------------------
// 쇼케이스
// ----------------------------------------------------------------------------

// 쇼케이스 뷰 모드. space는 v1.x 3D 전시관 (장기 계획)
enum ShowcaseViewMode {
  grid, // 현재 — 2D 그리드
  space, // 나중 — 3D 전시관 (Unity WebGL)
}

